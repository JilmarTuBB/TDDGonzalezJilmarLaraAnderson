    public int comboBox(JComboBox cmb, String value) {
        for (int i = 0; i < cmb.getItemCount(); i++) {
            String v = (String) cmb.getItemAt(i);
            if(v.equals(value)){
                return i;
            }else if(v.contains(value)) {
                return i;
            }
        }
        return 0;
    }
