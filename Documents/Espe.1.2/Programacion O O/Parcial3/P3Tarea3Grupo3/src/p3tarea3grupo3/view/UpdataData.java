    private void setData(){
        SearchData data = queryJson();
        ListClient listClient = ListClient.getInstance();
        List<Account> listAccounts = listClient.getAccount();
        for (Account acc : listAccounts) {
            if (acc.getId()== data.getIdentification()) {
            txtName.setText(acc.getClient().getName());
            txtLastName.setText(acc.getClient().getLastName());
            txtIdentification.setText(acc.getClient().getIdentification());
            txtCode.setText(acc.getClient().getCode());
            txtBirthDate.setText(acc.getClient().getBirthDate());
            cmbStatus.setSelectedIndex(comboBox(cmbStatus, acc.getClient().getStatus()));
            txtPhone.setText(String.valueOf(acc.getClient().getPhone()));
            txtAddress.setText(acc.getClient().getAddress());
            cmbOccupation.setSelectedIndex(comboBox(cmbOccupation, acc.getClient().getOccupation()));
            txtEmail.setText(acc.getClient().getEmail());
            txtNacionality.setText(acc.getClient().getNationality());
            cmbProvince.setSelectedIndex(comboBox(cmbProvince, acc.getClient().getProvince()));
            cmbCity.setSelectedIndex(comboBox(cmbCity, acc.getClient().getCity()));
            txtNameReference.setText(acc.getClient().getReferenceName());
            txtNumber.setText(String.valueOf(acc.getClient().getReferencePhone()));
            txtAccountNumber.setText(acc.getAccountNumber());
            txtTitular.setText(acc.getTitular());
            txtUser.setText(acc.getUser());
            txtPassword.setText(acc.getPassword());
            txtPIN.setText(acc.getPin());
            txtBalance.setText(Double.toString(acc.getBalance()));
            
            break;
            }
        }
        cleanRecord();
    }
    
