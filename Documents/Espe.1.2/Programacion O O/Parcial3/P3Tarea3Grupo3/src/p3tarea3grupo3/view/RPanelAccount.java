    private void registerMongo() {
        int id = accountList.size() + 1;
        String accountNumber = lblNumberCta.getText();
        String titular = client.getName() + " " + client.getLastName();
        String user = lblUser.getText();
        String password = txtPasword.getText();
        String pin = txtPin.getText();
        double balance = 1.0;
        
        Account ac = new Account(id, accountNumber, titular, user, password, pin, balance, client);
        Document doc = account.getDocument(ac);
        createMongo.addDocument(doc);
        
        
    }
    
    public String createUser(){
        String[] user = client.getName().split(" ");
        String nam = user[0].substring(0,1).toLowerCase() + user[1].substring(0,1).toLowerCase();
        String[]last = client.getLastName().split(" ");
        String lastn = last[0].toLowerCase();
        String userName = (nam+lastn);
        return userName;
    }
